namespace WholesaleStore.Identity.Configuration;

using Microsoft.AspNetCore.Diagnostics.HealthChecks;
using WholesaleStore.Common.HealthChecks;
using WholesaleStore.Identity.Configuration.HealthChecks;

public static class HealthCheckConfiguration
{
    public static IServiceCollection AddAppHealthChecks(this IServiceCollection services)
    {
        services.AddHealthChecks()
            .AddCheck<SelfHealthCheck>("WholesaleStore.Identity");

        return services;
    }

    public static void UseAppHealthChecks(this WebApplication app)
    {
        app.MapHealthChecks("/health");

        app.MapHealthChecks("/health/detail", new HealthCheckOptions
        {
            ResponseWriter = HealthCheckHelper.WriteHealthCheckResponse,
            AllowCachingResponses = false,
        });
    }
}
